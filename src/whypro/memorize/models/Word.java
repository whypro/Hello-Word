package whypro.memorize.models;

public class Word {
	public String name;
	public String phonetic;
	public String interp;
	
	public Word() {
		
	}
	
	public Word(String name, String phonetic, String interp) {
		this.name = name;
		this.phonetic = phonetic;
		this.interp = interp;
	}
}
